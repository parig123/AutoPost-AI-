    import json, os
    from datetime import datetime
    from services.ai_generator import AIGenerator
    from config import Config

    ai = AIGenerator(Config.GROQ_API_KEY)
    trends = ai.generate_trends()

    now = datetime.utcnow()
    cache_slot = f"{now.strftime('%Y-%m-%d')}-{(now.hour // 6) * 6:02d}"
    cache_path = os.path.join(os.path.dirname(os.path.abspath(__file__)), '..', 'trends_cache.json')
    try:
        with open(cache_path, 'w', encoding='utf-8') as f:
            json.dump({'slot': cache_slot, 'trends': trends}, f, ensure_ascii=False)
    except Exception:
        pass

    return jsonify(trends)
