from flask import Blueprint, jsonify, session
from flask_login import login_required, current_user
from models.database import db, Post, LinkedInAccount
from sqlalchemy import func
from datetime import datetime, timedelta

analytics_bp = Blueprint('analytics', __name__)

@analytics_bp.route('', methods=['GET'])
@login_required
def get_stats():
    active_account_id = session.get('active_account_id')

    query = Post.query.filter_by(user_id=current_user.id)
    if active_account_id:
        query = query.filter_by(linkedin_account_id=active_account_id)

    total_posts = query.count()
    published_posts = query.filter_by(status='Published').count()
    scheduled_posts = query.filter_by(status='Scheduled').count()
    failed_posts = query.filter_by(status='Failed').count()
    pending_approval = query.filter_by(status='Pending Approval').count()

    thirty_days_ago = datetime.utcnow() - timedelta(days=30)
    published_30d = query.filter(Post.status == 'Published', Post.created_at >= thirty_days_ago).count()

    success_rate = (published_posts / (published_posts + failed_posts) * 100) if (published_posts + failed_posts) > 0 else 0

    return jsonify({
        'total': total_posts,
        'published': published_posts,
        'published_30d': published_30d,
        'scheduled': scheduled_posts,
        'pending_approval': pending_approval,
        'failed': failed_posts,
        'success_rate': round(success_rate, 1)
    })


